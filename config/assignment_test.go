func TestRepoNamingHelpers(t *testing.T) {
	user := "alice"
	cfg := &AssignmentConfig{Course: "mpd", Name: "blatt01", UseCoursenameAsPrefix: true, UseEmailDomainAsSuffix: true}

	if got := cfg.RepoBaseName(); got != "mpd-blatt01" {
		t.Errorf("RepoBaseName() = %q", got)
	}
	if got := cfg.RepoNameWithSuffix("g1"); got != "mpd-blatt01-g1" {
		t.Errorf("RepoNameWithSuffix() = %q", got)
	}
	if got := cfg.RepoNameForStudent(&Student{Username: &user}); got != "mpd-blatt01-alice" {
		t.Errorf("RepoNameForStudent() = %q", got)
	}
	if got := cfg.RepoNameForGroup(&Group{Name: "team1"}); got != "mpd-blatt01-team1" {
		t.Errorf("RepoNameForGroup() = %q", got)
	}

	noPrefix := &AssignmentConfig{Course: "mpd", Name: "blatt01"}
	if got := noPrefix.RepoBaseName(); got != "blatt01" {
		t.Errorf("RepoBaseName() without prefix = %q, want %q", got, "blatt01")
	}
}

func TestGetAssignmentConfig(t *testing.T) {
	registerCourse(t, `
course:
  coursepath: mpd
  semesterpath: ss26
  useCoursenameAsPrefix: true
  students:
    - alice
    - "1001"
  a1:
    assignmentpath: blatt-01
    per: student
    description: blatt 1
    mergeRequest:
      mergeMethod: semi_linear
    release:
      dockerImages:
        - registry/app
`)
	cfg := mustAssignmentConfig(t, "course", "a1")

	if want := "mpd/ss26/blatt-01"; cfg.Path != want {
		t.Errorf("Path = %q, want %q", cfg.Path, want)
	}
	if want := goldenHost + "/mpd/ss26/blatt-01"; cfg.URL != want {
		t.Errorf("URL = %q, want %q", cfg.URL, want)
	}
	if cfg.Per != PerStudent {
		t.Errorf("Per = %q, want %q", cfg.Per, PerStudent)
	}
	// Configuring docker images implies the registry, without saying so.
	if !cfg.ContainerRegistry {
		t.Error("ContainerRegistry = false, want true when release docker images are configured")
	}
	if cfg.Release == nil || len(cfg.Release.DockerImages) != 1 {
		t.Errorf("Release = %#v, want one docker image", cfg.Release)
	}
	if cfg.Clone == nil {
		t.Error("Clone = nil, want defaults")
	}
	if len(cfg.Students) != 2 {
		t.Errorf("Students = %d, want 2", len(cfg.Students))
	}
	if cfg.AccessLevel != Developer {
		t.Errorf("AccessLevel = %v, want %v", cfg.AccessLevel, Developer)
	}
	if cfg.MergeRequest == nil || cfg.MergeRequest.MergeMethod != SemiLinearHistory {
		t.Errorf("MergeRequest = %#v, want mergeMethod %q", cfg.MergeRequest, SemiLinearHistory)
	}
}

func TestMergeRequestDefaults(t *testing.T) {
	registerCourse(t, `
course:
  a1:
    assignmentpath: blatt-01
`)
	mr := mustAssignmentConfig(t, "course", "a1").MergeRequest

	if mr == nil {
		t.Fatal("MergeRequest = nil, want defaults even when unconfigured")
	}
	if mr.MergeMethod != MergeCommit {
		t.Errorf("MergeMethod = %q, want %q", mr.MergeMethod, MergeCommit)
	}
	if mr.SquashOption != SquashDefaultOff {
		t.Errorf("SquashOption = %q, want %q", mr.SquashOption, SquashDefaultOff)
	}
	if mr.PipelineMustSucceed || mr.SkippedPipelinesAreSuccessful || mr.AllThreadsMustBeResolved || mr.StatusChecksMustSucceed {
		t.Errorf("MergeRequest checks = %#v, want all false by default", mr)
	}
	if mr.Approvals != nil || mr.ApprovalSettings != nil {
		t.Errorf("Approvals = %#v / %#v, want nil", mr.Approvals, mr.ApprovalSettings)
	}
}

func TestMergeRequestMergeMethodAndSquashOption(t *testing.T) {
	registerCourse(t, `
course:
  semi:
    mergeRequest: {mergeMethod: semi_linear}
  ff:
    mergeRequest: {mergeMethod: ff}
  merge:
    mergeRequest: {mergeMethod: merge}
  bogus:
    mergeRequest: {mergeMethod: nonsense}
  never:
    mergeRequest: {squashOption: never}
  always:
    mergeRequest: {squashOption: always}
  defaulton:
    mergeRequest: {squashOption: default_on}
  defaultoff:
    mergeRequest: {squashOption: default_off}
  bogussquash:
    mergeRequest: {squashOption: nonsense}
`)

	for _, tt := range []struct {
		assignment string
		want       MergeMethod
	}{
		{"semi", SemiLinearHistory},
		{"ff", FastForward},
		{"merge", MergeCommit},
		{"bogus", MergeCommit},
	} {
		if got := mustAssignmentConfig(t, "course", tt.assignment).MergeRequest.MergeMethod; got != tt.want {
			t.Errorf("%s: MergeMethod = %q, want %q", tt.assignment, got, tt.want)
		}
	}

	for _, tt := range []struct {
		assignment string
		want       SquashOption
	}{
		{"never", SquashNever},
		{"always", SquashAlways},
		{"defaulton", SquashDefaultOn},
		{"defaultoff", SquashDefaultOff},
		{"bogussquash", SquashDefaultOff},
	} {
		if got := mustAssignmentConfig(t, "course", tt.assignment).MergeRequest.SquashOption; got != tt.want {
			t.Errorf("%s: SquashOption = %q, want %q", tt.assignment, got, tt.want)
		}
	}
}

