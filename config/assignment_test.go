// UseEmailDomainAsSuffix now means what it says: false selects the local part.
//
// It used to take a second reading of viper to decide, so that a zero-valued
// struct meant "use the domain" — the field being false was not enough, viper
// also had to say the key was unset. That crutch is gone with course files
// leaving viper, and resolution always sets the field (absent means true), so
// the only thing that changes is what a hand-built config means. Nothing but the
// resolver builds one.
func TestRepoSuffix(t *testing.T) {
	id := 123
	mail := "a@b.example"
	user := "alice"

	withDomain := &AssignmentConfig{Course: "mpd", Name: "blatt01", UseCoursenameAsPrefix: true, UseEmailDomainAsSuffix: true}
	if got := withDomain.RepoSuffix(&Student{Email: &mail}); got != "a_at_b.example" {
		t.Errorf("RepoSuffix(email) with domain = %q, want %q", got, "a_at_b.example")
	}

	localOnly := &AssignmentConfig{Course: "mpd", Name: "blatt01", UseCoursenameAsPrefix: true}
	if got := localOnly.RepoSuffix(&Student{Email: &mail}); got != "a" {
		t.Errorf("RepoSuffix(email) without domain = %q, want %q", got, "a")
	}

	for _, tt := range []struct {
		name    string
		student *Student
		want    string
	}{
		{"id", &Student{Id: &id}, "123"},
		{"username", &Student{Username: &user}, "alice"},
		{"empty", &Student{}, ""},
	} {
		if got := withDomain.RepoSuffix(tt.student); got != tt.want {
			t.Errorf("RepoSuffix(%s) = %q, want %q", tt.name, got, tt.want)
		}
	}
}

// The default resolves to true, so an assignment that says nothing keeps the
// domain in the repository name.
func TestUseEmailDomainAsSuffixDefaultsToTrue(t *testing.T) {
	registerCourse(t, `
course:
  students:
    - a@b.example
  a1:
    assignmentpath: blatt-01
`)
	cfg := mustAssignmentConfig(t, "course", "a1")

	if !cfg.UseEmailDomainAsSuffix {
		t.Fatal("UseEmailDomainAsSuffix = false, want true when the course says nothing")
	}
	if got := cfg.RepoSuffix(cfg.Students[0]); got != "a_at_b.example" {
		t.Errorf("RepoSuffix = %q, want %q", got, "a_at_b.example")
	}
}

