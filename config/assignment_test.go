func TestMergeRequestApprovals(t *testing.T) {
	registerCourse(t, `
course:
  a1:
    mergeRequest:
      approvals:
        settings:
          preventApprovalByMergeRequestCreator: true
          preventApprovalsByUsersWhoAddCommits: false
          whenCommitAdded: removeAllApprovals
        rules:
          - name: review
            branches: [main, startercode]
            requiredApprovals: 2
            multiMemberGroupsOnly: true
          - name: submission
            branch: startercode
            usernames: [obcode]
            requiredApprovals: 1
`)
	mr := mustAssignmentConfig(t, "course", "a1").MergeRequest

	if len(mr.Approvals) != 2 {
		t.Fatalf("Approvals = %d rules, want 2", len(mr.Approvals))
	}
	if got := mr.Approvals[0]; got.Name != "review" || len(got.Branches) != 2 || got.RequiredApprovals != 2 || !got.MultiMemberGroupsOnly {
		t.Errorf("rule 0 = %#v", got)
	}
	// The deprecated singular `branch` is folded into Branches.
	if got := mr.Approvals[1]; len(got.Branches) != 1 || got.Branches[0] != "startercode" || got.Branch != "" {
		t.Errorf("rule 1 = %#v, want the singular branch folded into Branches", got)
	}

	s := mr.ApprovalSettings
	if s == nil {
		t.Fatal("ApprovalSettings = nil")
	}
	if s.PreventApprovalByMergeRequestCreator == nil || !*s.PreventApprovalByMergeRequestCreator {
		t.Errorf("PreventApprovalByMergeRequestCreator = %v, want true", s.PreventApprovalByMergeRequestCreator)
	}
	if s.PreventApprovalsByUsersWhoAddCommits == nil || *s.PreventApprovalsByUsersWhoAddCommits {
		t.Errorf("PreventApprovalsByUsersWhoAddCommits = %v, want an explicit false", s.PreventApprovalsByUsersWhoAddCommits)
	}
	// Unmentioned settings stay nil so they are never sent to GitLab.
	if s.RequireUserReauthenticationToApprove != nil {
		t.Errorf("RequireUserReauthenticationToApprove = %v, want nil when unconfigured", *s.RequireUserReauthenticationToApprove)
	}
	if s.WhenCommitAdded == nil || *s.WhenCommitAdded != ApprovalRemoveAllApprovals {
		t.Errorf("WhenCommitAdded = %v, want %q", s.WhenCommitAdded, ApprovalRemoveAllApprovals)
	}
}
